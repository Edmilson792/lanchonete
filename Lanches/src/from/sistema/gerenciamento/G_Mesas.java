    private void jTextFieldPesquisarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldPesquisarKeyReleased

        String campoPesquisa = jTextFieldPesquisar.getText().trim();

        if (!campoPesquisa.equals("")) {
            DefaultTableModel modelo = (DefaultTableModel) jTableInformacoesMesa.getModel();
            modelo.setNumRows(0);

            try {
                Connection con = Conexao.getInstancia();
                String sql = "SELECT * FROM mesas WHERE numero LIKE ?";
                PreparedStatement ps = con.prepareStatement(sql);

                ps.setString(1, campoPesquisa + "%");
                ResultSet rs = ps.executeQuery();

                while (rs.next()) {
                    String[] novaLinha = {
                        String.valueOf(rs.getInt("id_mesas")),
                        rs.getString("numero"),
                        rs.getString("tipo"),
                        rs.getString("referencias"),
                    };
                    modelo.addRow(novaLinha);
                }
                rs.close();
                ps.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro na consulta: " + e.getMessage());
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jTextFieldPesquisarKeyReleased

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed

        int btDialog = JOptionPane.YES_NO_OPTION;
        int opcao = JOptionPane.showConfirmDialog(null, "Confirmar exclusão?", "Exclusão", btDialog);

        if (opcao == 0) {
            try {

                int linhaSelecionada = jTableInformacoesMesa.getSelectedRow();

                Integer id = Integer.parseInt(jTableInformacoesMesa.getModel().
                        getValueAt(linhaSelecionada, 0).toString());

                Connection con = Conexao.getInstancia();
                String sql = "DELETE FROM mesas WHERE id_mesas = ?";
                PreparedStatement ps = con.prepareStatement(sql);

                ps.setInt(1, id);
                ps.executeUpdate();

                DefaultTableModel modelo = (DefaultTableModel) jTableInformacoesMesa.getModel();
                modelo.removeRow(linhaSelecionada);

                JOptionPane.showMessageDialog(null, "Excluido com Sucesso!");
                jTextFieldPesquisar.setText("");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro ao excluir: " + e.getMessage());
            }
        }
        
    }//GEN-LAST:event_jButtonExcluirActionPerformed

    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed

        Integer linhaSelecionada = jTableInformacoesMesa.getSelectedRow();

        if (linhaSelecionada != -1) {
            try {

                Integer id = Integer.parseInt(jTableInformacoesMesa.getModel()
                        .getValueAt(linhaSelecionada, 0).toString());

                AltMesas altMesas = new AltMesas();
                altMesas.setVisible(true);
                altMesas.setIdMesasAlterar(id);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro não esperado: " + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma Mesa!");
        }
        
    }//GEN-LAST:event_jButtonAlterarActionPerformed

