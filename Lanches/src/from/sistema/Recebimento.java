    private void jButtonFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonFinalizarActionPerformed

        int st = 0;

        try {
            for (int linha = 0; linha < jTableDados.getRowCount(); linha++) {
                String sql = "INSERT INTO recebimento ("
                        + "codigoPedido, "
                        + "produtos, "
                        + "quantidade, "
                        + "valorUnitario, "
                        + "totalParcial, "
                        + "id_mesas) "
                        + "VALUES (?, ?, ?, ?, ?, ?)";

                Connection con = Conexao.getInstancia();
                PreparedStatement ps = con.prepareStatement(sql);

                DefaultTableModel tabela = (DefaultTableModel) jTableDados.getModel();

                ps.setInt(1, Integer.parseInt(tabela.getValueAt(linha, 0).toString()));
                ps.setString(2, tabela.getValueAt(linha, 2).toString());
                ps.setInt(3, Integer.parseInt(tabela.getValueAt(linha, 3).toString()));
                ps.setDouble(4, Double.parseDouble(tabela.getValueAt(linha, 4).toString()));
                ps.setDouble(5, Double.parseDouble(tabela.getValueAt(linha, 5).toString()));
                ps.setInt(6, Integer.parseInt(tabela.getValueAt(linha, 1).toString()));

                ps.executeUpdate();
                st++;
                ps.close();
            }
            if (st == 0) {
                JOptionPane.showMessageDialog(null, "Escolha um pedido");
            } else {
                JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso");
            }

            valorRecebidoCaixa = Double.parseDouble(jTextFieldTotalVenda.getText());
            valorTotalCaixa = valorTotalCaixa + Double.parseDouble(jTextFieldTotalVenda.getText());

            LimparTable();
            jTextFieldTotalVenda.setText("");

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro não esperado: " + e.getMessage());
        }

    }//GEN-LAST:event_jButtonFinalizarActionPerformed

