    void montagrupo() {

        String sql = "SELECT m.id_mesas, m.numero FROM pedidos p, mesas m WHERE m.id_mesas = p.id_mesas AND p.status = 0 GROUP BY m.numero";

        Connection con;
        try {
            JPanel painelMesa = new JPanel();
            con = Conexao.getInstancia();
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                int tamanho = rs.getFetchSize();
                painelMesa.setLayout(new GridLayout(tamanho, 5, 1, 1));
                painelMesa.setEnabled(false);
                try {
                    rs.beforeFirst();
                    int x = 0;
                    while (rs.next()) {
                        //  System.out.println("ererer" + rs.getString("nomesubgrupo"));
                        JButton jbutton = new JButton(rs.getString("numero"));
                        jbutton.setSize(100, 30);
                        jbutton.setActionCommand(String.valueOf(x));
                        jbutton.addActionListener(this);
                        //codgrupos.add(rs.getInt("id_mesas"));
                        botoesGrupo.add(jbutton);
                        idsMesa.add(rs.getInt("id_mesas"));
                        x++;
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
                for (JButton j : botoesGrupo) {
                    painelMesa.add(j);
                }
            } else {
                jPanelPedidosAtivos.setBorder(javax.swing.BorderFactory.createTitledBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(0, 0, 0)
                ), "Mesa Vazia", 0, 2, new java.awt.Font("Serif", 1, 12),
                        new java.awt.Color(0, 146, 156))); // NOI18N
            }
            jPanelPedidosAtivos.add(new JScrollPane(painelMesa), BorderLayout.CENTER);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro: " + ex.getMessage());
        }
    }

    @Override
    public void actionPerformed(ActionEvent e) {
        Integer idMesa = idsMesa.get(Integer.parseInt(e.getActionCommand()));
        try {
            DefaultTableModel modelo = (DefaultTableModel) jTableDados.getModel();
            modelo.setNumRows(0);

            String sql = "SELECT p.*, pr.nome FROM pedidos p, produtos pr "
                    + "WHERE p.id_produtos = pr.id_produtos AND p.id_mesas = ?"
                    + " AND status = false";

            Connection con = Conexao.getInstancia();
            PreparedStatement ps = con.prepareStatement(sql);

            ps.setInt(1, idMesa);

            ResultSet rs = ps.executeQuery();

            Double total = 0.0;
            if (op) {
                while (rs.next()) {
                    String[] novaLinha = {
                        String.valueOf(rs.getInt("id_pedidos")),
                        String.valueOf(rs.getInt("id_mesas")),
                        rs.getString("nome"),
                        String.valueOf(rs.getInt("qtd")),
                        A.formataValor(rs.getDouble("valUnit")),
                        String.valueOf(A.formataValor(rs.getInt("qtd") * rs.getDouble("valUnit")))
                    };
                    modelo.addRow(novaLinha);
                    total = total + rs.getInt("qtd") * rs.getDouble("valUnit");
                }
                jTextFieldTotalVenda.setText(A.formataValor(total));

                rs.close();
                ps.close();
            } else if (op == false) {
                rs.close();
                ps.close();
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro não esperado: " + ex.getMessage());
            ex.printStackTrace();
        }
    }
