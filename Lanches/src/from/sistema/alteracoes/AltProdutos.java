    public void setIdProdAlterar(Integer id) throws Exception {
        
        try {
            Connection con = Conexao.getInstancia();

            String sql = "SELECT * FROM produtos WHERE id_Produtos = ?";
            PreparedStatement ps = con.prepareStatement(sql);

            ps.setInt(1, id);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                jTextFieldCodigo.setText(id.toString());
                jTextFieldNome.setText(rs.getString("nome"));
                jTextFieldValUnit.setText(rs.getString("valUnit"));
                jTextFieldMarca.setText(rs.getString("marca"));
                jTextAreaComposicao.setText(rs.getString("composicao"));
            }
            
            rs.close();
            ps.close();
            
        } catch (Exception e) {
            throw e;
        }
        
    }
