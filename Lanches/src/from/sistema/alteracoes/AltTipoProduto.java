    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed

        if (jTextFieldCodigo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Informe o código para alteração.");
            jTextFieldCodigo.requestFocus();
        } else {
            try {
                String sql = "UPDATE tipoProduto SET tipo = ? WHERE id_tipoProduto = ?";

                Connection con = Conexao.getInstancia();
                PreparedStatement ps = con.prepareStatement(sql);

                int dialogButton = JOptionPane.YES_NO_OPTION;
                int result = JOptionPane.showConfirmDialog(null,
                    "Alterar os Dados?", "Alteração!", dialogButton);
                if (result == 0) {
                    ps.setString(1, jTextFieldTipo.getText());
                    ps.setInt(2, Integer.parseInt(jTextFieldCodigo.getText()));

                    ps.executeUpdate();
                    ps.close();

                    JOptionPane.showMessageDialog(null, "Alterações realizadas com sucesso.");
                    jTextFieldCodigo.setText("");
                    jTextFieldTipo.setText("");
                    jTextFieldCodigo.requestFocus();
                } else {
                    JOptionPane.showMessageDialog(null, "Alteraçãos Cancelada.");
                    jTextFieldCodigo.requestFocus();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro não esperado: "
                    + "" + e.getMessage());
            }
        }
    }//GEN-LAST:event_jButtonAlterarActionPerformed

    private void jTextFieldCodigoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextFieldCodigoFocusLost

        if (!jTextFieldCodigo.getText().equals("")) {
            try {
                PreparedStatement ps;
                Connection con = Conexao.getInstancia();

                String sql = "SELECT * FROM tipoProduto WHERE id_tipoProduto = ?";
                ps = con.prepareStatement(sql);

                int id = Integer.parseInt(jTextFieldCodigo.getText());
                ps.setInt(1, id);

                ResultSet rs = ps.executeQuery();

                int cont = 0;
                while (rs.next()) {
                    cont++;
                    jTextFieldTipo.setText(rs.getString("tipo"));
                }
                if (cont == 0) {
                    ps.close();
                    JOptionPane.showMessageDialog(null, "Tipo de Produto não existe");
                    jTextFieldCodigo.setText("");
                    jTextFieldCodigo.requestFocus();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "erro: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_jTextFieldCodigoFocusLost

