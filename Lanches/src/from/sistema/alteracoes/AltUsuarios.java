    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed

        if (jTextFieldCodigo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Informe o código para alteração.");
            jTextFieldCodigo.requestFocus();
        } else if (jPasswordFieldSenha.getText().equals("")
            || jPasswordFieldConfirmSenha.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha todos "
                + "os campos para alteração!");
            jPasswordFieldSenha.requestFocus();
        } else {
            String senha = jPasswordFieldSenha.getText();
            try {
                senha = Seguranca.hash(senha);

                String sql = "UPDATE usuarios SET "
                + "nome = ?, "
                + "telefone = ?, "
                + "email = ?, "
                + "usuario = ?, "
                + "senha = ? "
                + "WHERE id_usuario = ?";

                Connection con = Conexao.getInstancia();
                PreparedStatement ps = con.prepareStatement(sql);

                int dialogButton = JOptionPane.YES_NO_OPTION;
                int result = JOptionPane.showConfirmDialog(null,
                    "Alterar os Dados?", "Confirmar Operação!", dialogButton);
                if (result == 0) {
                    ps.setString(1, jTextFieldNome.getText());
                    ps.setString(2, jFormattedTextFieldTelefone.getText());
                    ps.setString(3, jTextFieldEmail.getText());
                    ps.setString(4, jTextFieldUsuario.getText());
                    ps.setString(5, senha);
                    ps.setInt(6, Integer.parseInt(jTextFieldCodigo.getText()));

                    ps.executeUpdate();
                    ps.close();

                    JOptionPane.showMessageDialog(null, "Alterações realizadas com sucesso.");
                    limparTela();
                    jTextFieldCodigo.requestFocus();
                } else {
                    JOptionPane.showMessageDialog(null, "Alterações Canceladas.");
                    limparTela();
                    jTextFieldCodigo.requestFocus();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro não esperado: "
                    + "" + e.getMessage());
            }
        }
    }//GEN-LAST:event_jButtonAlterarActionPerformed

    private void jTextFieldCodigoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextFieldCodigoFocusLost

        if (!jTextFieldCodigo.getText().equals("")) {
            try {
                PreparedStatement ps;
                Connection con = Conexao.getInstancia();

                String sql = "SELECT * FROM usuarios WHERE id_usuario = ?";
                ps = con.prepareStatement(sql);

                int codigo = Integer.parseInt(jTextFieldCodigo.getText());
                ps.setInt(1, codigo);

                ResultSet rs = ps.executeQuery();

                int cont = 0;
                while (rs.next()) {
                    cont++;
                    jTextFieldNome.setText(rs.getString("nome"));
                    jFormattedTextFieldTelefone.setText(rs.getString("telefone"));
                    jTextFieldEmail.setText(rs.getString("email"));
                    jTextFieldUsuario.setText(rs.getString("usuario"));
                }
                if (cont == 0) {
                    ps.close();
                    JOptionPane.showMessageDialog(null, "Usuário não existe");
                    jTextFieldCodigo.setText("");
                    jTextFieldCodigo.requestFocus();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "erro: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_jTextFieldCodigoFocusLost

