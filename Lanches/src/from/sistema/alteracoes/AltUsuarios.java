    public void setIdUsuariosAlterar(Integer id) throws Exception {
        
        try {
            Connection con = Conexao.getInstancia();

            String sql = "SELECT * FROM usuarios WHERE id_usuario = ?";
            PreparedStatement ps = con.prepareStatement(sql);

            ps.setInt(1, id);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                jTextFieldCodigo.setText(id.toString());
                jTextFieldNome.setText(rs.getString("nome"));
                jFormattedTextFieldTelefone.setText(rs.getString("telefone"));
                jTextFieldEmail.setText(rs.getString("email"));
                jTextFieldUsuario.setText(rs.getString("usuario"));
            }
            rs.close();
            ps.close();
        } catch (Exception e) {
            throw e;
        }
        
    }

