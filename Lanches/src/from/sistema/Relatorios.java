    private void jButtonProdutosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonProdutosActionPerformed

        try {
            String caminho = "relatorios/relatorioProdutos.jasper";
            String nomeArquivo = "relatorioProdutos.pdf";

            JasperReport jas = (JasperReport) JRLoader.loadObjectFromFile(caminho);

            JasperPrint jasprint = JasperFillManager.fillReport(jas, null, Conexao.getInstancia());

            JRExporter exporter = new JRPdfExporter();
            exporter.setParameter(JRExporterParameter.OUTPUT_FILE_NAME, nomeArquivo);
            exporter.setParameter(JRExporterParameter.JASPER_PRINT, jasprint);
            exporter.exportReport();

            File myFile = new File(nomeArquivo);
            Desktop.getDesktop().open(myFile);

        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao gerar relat贸rio: \n"
                    + ex.getMessage());
        }
        
    }//GEN-LAST:event_jButtonProdutosActionPerformed

