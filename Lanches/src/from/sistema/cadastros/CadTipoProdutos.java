    private void jButtonAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAdicionarActionPerformed

        try {
            String tipoP = jTextFieldTiposProdutos.getText();

            if (jTextFieldTiposProdutos.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(null, "Preencha o campo!");
                jTextFieldTiposProdutos.requestFocus();
            } else {
                if (!TipoProdutoDao.existeTipoProduto(tipoP)) {

                    String sql = "INSERT INTO tipoProduto(tipo) VALUES (?)";

                    Connection con = Conexao.getInstancia();
                    PreparedStatement ps = con.prepareStatement(sql);

                    ps.setString(1, tipoP);

                    ps.executeUpdate();
                    ps.close();

                    JOptionPane.showMessageDialog(null, "Adicionado!");
                    jTextFieldTiposProdutos.setText("");
                    jTextFieldTiposProdutos.requestFocus();
                    
                    form.carregaComboTipoProduto();
                } else {
                    JOptionPane.showMessageDialog(null, "Tipo de Produto j√° existente!");
                    jTextFieldTiposProdutos.requestFocus();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_jButtonAdicionarActionPerformed

